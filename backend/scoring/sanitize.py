"""
backend/scoring/sanitize.py

Sanitization helpers that translate internal database codes and enum values
into human-readable strings before they appear in any user-facing text.

All text generated by the scoring engine (explanation, top_risks, titles) MUST
pass through sanitize_title() before it reaches the API response.

Sources of internal codes:
  - Chicago CDOT street closure work_type codes (e.g. "GenOpening", "GenOccupy")
  - Chicago building permit type strings (e.g. "EASY PERMIT PROGRAM")
  - ISO date strings in explanation text
  - Meter distances in text

Frontend maintains a parallel copy of these mappings in
frontend/src/lib/text-sanitize.ts as a defence-in-depth layer.
Keep both files in sync when adding new codes.
"""

